def pad_lower(letter):
    c = [[0]*8 for i in range(8)]
    for j in range(4):
        for i in range(4):
            c[j+4][i] = letter[j][i]

    return c

def make_upper(letter):
    c = [[0]*8 for i in range(8)]
    for j in range(4):
        for i in range(4):
            c[j*2][i*2] = letter[j][i]
            c[j*2 + 1][i*2] = letter[j][i]
            c[j*2][i*2 + 1] = letter[j][i]
            c[j*2 + 1][i*2 + 1] = letter[j][i]

    return c

